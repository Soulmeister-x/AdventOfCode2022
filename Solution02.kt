fun main() {
    val strategies = input.split("\n")
    var score = 0

    for (s in strategies) {
        score += calcScore(s.split(" "))
    }
    
    println("strategy returns score of $score")
}

fun calcScore(shapeInputs: List<String>): Int {
    val elvShape = shapeInputs[0]
    val shouldResult = shapeInputs[1]
    val userShape = getFittingShape(elvShape, shouldResult)

    val value = getShapeValue(userShape)
    val score = getWinningScore(elvShape, userShape)
    return value + score
}

fun getFittingShape(elv: String, shouldResult: String): String {
    return when (shouldResult) {
        "Z" -> if (elv == "A") "Y" else if (elv == "B") "Z" else "X"
        "Y" -> if (elv == "A") "X" else if (elv == "B") "Y" else "Z"
        else -> if (elv == "A") "Z" else if (elv == "B") "X" else "Y"
    }
}

fun getWinningScore(elv: String, user: String): Int {
    val score = when (elv) {
        "A" -> if (user == "Y") 6 else if (user == "X") 3 else 0
        "B" -> if (user == "Z") 6 else if (user == "Y") 3 else 0
        else -> if (user == "X") 6 else if (user == "Z") 3 else 0
    }
    return score
}

fun getShapeValue(shape: String): Int {
    return if (shape == "Z") 3 else if (shape == "Y") 2 else 1
}

